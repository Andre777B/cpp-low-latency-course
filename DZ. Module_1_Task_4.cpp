/* 4. Показать, как меняется размер структуры при перестановке полей, и объяснить, почему это важно*/


#include <iostream>

/*
    В первой структуре поля расположены неудачно:
    сначала char, затем double и int.

    Компилятор может добавить пустые байты между полями,
    чтобы double и int находились по правильным адресам.
*/
struct BadLayout
{
    char symbol;
    double price;
    int id;
};

/*
    Во второй структуре те же поля,
    но они расположены от большего типа к меньшему.

    Такой порядок часто уменьшает количество
    дополнительных пустых байтов.
*/
struct BetterLayout
{
    double price;
    int id;
    char symbol;
};

int main()
{
    /*
        sizeof показывает полный размер структуры в байтах.
        В размер входят не только поля,
        но и дополнительные байты выравнивания.
    */
    std::cout << "BadLayout size: "
        << sizeof(BadLayout)
        << " bytes\n";

    std::cout << "BetterLayout size: "
        << sizeof(BetterLayout)
        << " bytes\n\n";

    /*
        alignof показывает требование к выравниванию типа.
        Например, значение 8 означает,
        что объект обычно должен начинаться с адреса,
        кратного восьми.
    */
    std::cout << "BadLayout alignment: "
        << alignof(BadLayout)
        << " bytes\n";

    std::cout << "BetterLayout alignment: "
        << alignof(BetterLayout)
        << " bytes\n";

    /*
        Размеры могут зависеть от компилятора,
        платформы и настроек сборки.

        Главный вывод:
        порядок одинаковых полей может изменить
        общий размер структуры.
    */

    return 0;
}


/*Ответ: меньшая структура занимает меньше памяти, и в кэш процессора помещается больше объектов. 
В программах, где таких объектов тысячи или миллионы, 
правильный порядок полей может уменьшить расход памяти и ускорить обработку данных.*/